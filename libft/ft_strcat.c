char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	y;

	i = 0;
	y = 0;
	if (*s2 == '\0')
		return (s1);
	while (s1[y] != '\0')
		++y;
	while (s2[i] != '\0')
	{
		s1[y] = s2[i];
		++i;
		++y;
	}
	s1[y] = '\0';
	return (s1);
}
