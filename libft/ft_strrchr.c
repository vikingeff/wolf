char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0 && s[i] != c)
		i--;
	if (s[i] != c)
		return (0);
	return ((char *)&s[i]);
}
