#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n < 0)
	{
		n = n * -1;
		ft_putchar('-');
	}
	if (n >= 10)
	{
		i = n / 10;
		ft_putnbr(i);
		ft_putchar('0' + n % 10);
	}
	else
		ft_putchar('0' + n);
}
